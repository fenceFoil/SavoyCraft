package com.savoycraft.opera;

import org.w3c.dom.Element;
import org.w3c.dom.NodeList;

/**
 *
 */
public class Character {

	private String name = "";
	private String handle = "";

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getHandle() {
		return handle;
	}

	public void setHandle(String handle) {
		this.handle = handle;
	}

	public static Character load(Element characterElement) {
		NodeList characterNodes = characterElement.getChildNodes();

		Character character = new Character();
		character.setHandle(Opera.getTextElement("handle", characterNodes));
		character.setName(Opera.getTextElement("name", characterNodes));
		
		return character;
	}

}
